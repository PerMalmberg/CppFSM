#include "FSM.h"

namespace fsm {

///////////////////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////////////////
void
BaseState::AddToEnterChain( IEnterChain *chain )
{
	myEnterChain.push_back( chain );
}

///////////////////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////////////////
void
BaseState::AddToLeaveChain( ILeaveChain *chain )
{
	myLeaveChain.push_front( chain );
}

///////////////////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////////////////
void
BaseState::DoEnter()
{
	for( auto state : myEnterChain )
	{
		state->DoEnter();
	}
}

///////////////////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////////////////
void
BaseState::DoLeave()
{
	for( auto state : myLeaveChain )
	{
		state->DoLeave();
	}
}

}