#include <algorithm>
#include <FSM.h>

namespace fsm {

///////////////////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////////////////
FSM::FSM()
{
}

///////////////////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////////////////
FSM::~FSM()
{
	myCurrent.reset();
}


///////////////////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////////////////
void FSM::SetState(std::unique_ptr<BaseState> newState)
{
	if( myCurrent )
	{
		myCurrent->DoLeave();
	}

	myCurrent = std::move(newState);
	myCurrent->DoEnter();
}

}