#include <algorithm>
#include <FSM.h>

namespace fsm {

///////////////////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////////////////
FSM::FSM() {
}

///////////////////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////////////////
FSM::~FSM() {
    myCurrent.reset();
}


///////////////////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////////////////
void FSM::SetState(std::unique_ptr<IBaseState> newState) {
    if (myCurrent) {
        myCurrent->Leave();
    }

    myCurrent = std::move(newState);
    myCurrent->Enter();
}

}